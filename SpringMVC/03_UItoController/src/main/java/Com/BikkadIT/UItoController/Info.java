package Com.BikkadIT.UItoController;

public class Info {
/*
 * In web application we will perform 2 operations:

1)Sending data from Controller to UI

2)Sending  data from UI to controller

1)Sending data from Controller to UI
====================================
To Sending data from Controller to UI we will use Model or ModelAndView

Model is Map which represents data in the form of key value pair.

model.addAttribute(String key,Object value)

We can get value using key.


2) Sending data from UI to controller:
======================================
To Send data from UI To controller we will have below options.

1)Query parameters
2)Path parameters
3)Request Body



1)Query parameters:Query parameter will represent data in Url in the form of key-value pair.
==================
    Ex:http://localhost:9090/wel?name=Santosh

Query parameters will always starts with ? symbol.

  Ex:Ex:http://localhost:9090/course?name=javaDevelopment&trainer=santosh

Query parameters will be seperated by & symbol

Query parameters will present only at end of the URL.

To read Query parameter value we will use @Requestparam Annotation.

 */
}
